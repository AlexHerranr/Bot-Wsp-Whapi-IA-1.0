// Function to run tests
function runTestCategory(category) {
  console.log(`\nğŸ“‹ Running: ${category.name}`);
  console.log(`ğŸ“ ${category.description}\n`);

  try {
    const command = `npm test -- "${category.pattern}" --verbose --passWithNoTests`;
    console.log(`ğŸ”¨ Command: ${command}\n`);
    
    const output = execSync(command, { 
      encoding: 'utf8',
      stdio: 'pipe'
    });
    
    console.log(output);
    console.log(`âœ… ${category.name} - PASSED\n`);
    return true;
  } catch (error) {
    console.error(`âŒ ${category.name} - FAILED`);
    console.error(error.stdout || error.message);
    console.log('\n');
    return false;
  }
}

// Function to run all tests with coverage
function runAllTestsWithCoverage() {
  console.log('\nğŸ“Š Running All CRM Tests with Coverage...\n');
  
  try {
    const command = 'npm test -- --testPathPatterns=crm --coverage --coverageDirectory=tests/reports/coverage-crm';
    console.log(`ğŸ”¨ Coverage Command: ${command}\n`);
    
    const output = execSync(command, { 
      encoding: 'utf8',
      stdio: 'pipe'
    });
    
    console.log(output);
    console.log('âœ… Coverage Report Generated\n');
    return true;
  } catch (error) {
    console.error('âŒ Coverage Generation Failed');
    console.error(error.stdout || error.message);
    return false;
  }
}

